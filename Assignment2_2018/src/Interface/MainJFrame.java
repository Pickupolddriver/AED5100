    private void ReadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReadBtnActionPerformed
        // TODO add your handling code here:
        String csvFile = "/Users/lijianxi/Desktop/MOCK_DATA.csv";
        BufferedReader br = null;
        String line = "";
        String cvsSplitBy = ",";

        try {
            br = new BufferedReader(new FileReader(csvFile));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while ((line = br.readLine()) != null) {
                String[] colum = line.split(cvsSplitBy);
                String serialnum = colum[0];
                String manufacture = colum[1];
                String model = colum[2];
                int seatnum = Integer.parseInt(colum[3]);
                int manuyear = Integer.parseInt(colum[4]);
                String update = colum[5];
                String expdate = colum[6];
                boolean available;
                String ava = colum[7];
                if(ava.equals("TRUE")){
                    available=true;
                }else{
                    available=false;
                }
                String city = colum[8];
                double latitude = Double.parseDouble(colum[9]);
                double longitude = Double.parseDouble(colum[10]);

                Vehicle v0 = vh.addViechle();
                v0.setSerialnumber(serialnum);
                v0.setManufacturer(manufacture);
                v0.setManufactureyear(manuyear);
                v0.setSeatnumber(seatnum);
                v0.setModel(model);
                try {
                    //v1.setUpdatedate(updatedate);
                    v0.setUpdatedate(update);
                } catch (ParseException ex) {
                    Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    v0.setExpiredDate(expdate);
                } catch (ParseException ex) {
                    Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
                v0.setAvailable(available);
                v0.setLatitue(latitude);
                v0.setLongitude(longitude);
                v0.setCity(city);

            }
        } catch (IOException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ReadBtnActionPerformed

