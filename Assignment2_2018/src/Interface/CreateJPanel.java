    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed

        Vehicle v1 = vh.addViechle();
        String manufacture = ManufactureTxt.getText();
        int manuYear = Integer.parseInt(ManuYearTxt.getText());
        int seatNum = Integer.parseInt(SeatNumTxt.getText());
        String SerialNum = SerialNumTxt.getText();
        String modle = ModelTxt.getText();
        
        String updatedate = UpdateDateTxt.getText();
        if(updatedate.equals(null)){
            JOptionPane.showMessageDialog(null, "pls enter the updatedate");
        }
        String ExpDate = ExpDateTxt.getText();
        
        Boolean available = AvailableCheckBox.isSelected();
        Double latitude = Double.parseDouble(LatitudeTxt.getText());
        Double longitude = Double.parseDouble(LongitudeTxt.getText());
        String city=CityTxt.getText();

        v1.setManufacturer(manufacture);
        v1.setManufactureyear(manuYear);
        v1.setSeatnumber(seatNum);
        v1.setSerialnumber(SerialNum);
        v1.setModel(modle);
        try {
            //v1.setUpdatedate(updatedate);
            v1.setUpdatedate(updatedate);
        } catch (ParseException ex) {
            Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            v1.setExpiredDate(ExpDate);
        } catch (ParseException ex) {
            Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        v1.setAvailable(available);
        v1.setLatitue(latitude);
        v1.setLongitude(longitude);
        v1.setCity(city);

        JOptionPane.showMessageDialog(null, "Viechles Added Successsfully");
        clearAllTextField();

        // TODO add your handling code here:
    }//GEN-LAST:event_SaveBtnActionPerformed

